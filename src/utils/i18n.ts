/**
 * Automatically translates disease name strings between English and Hindi.
 */
export function localizeDiseaseName(name: string, lang: Language): string {
  if (!name) return name;
  const trimmed = name.trim();

  // 1. Direct key lookup
  if (diseaseTranslations[trimmed]) {
    return diseaseTranslations[trimmed][lang];
  }

  // 2. Case-insensitive lookup
  const lower = trimmed.toLowerCase();
  for (const [key, value] of Object.entries(diseaseTranslations)) {
    if (key.toLowerCase() === lower) {
      return value[lang];
    }
  }

  // 3. Substring replacement for composite names
  let localized = name;
  for (const [, value] of Object.entries(diseaseTranslations)) {
    if (lang === 'en') {
      localized = localized.replace(new RegExp(value.hi, 'g'), value.en);
    } else {
      localized = localized.replace(new RegExp(value.en, 'g'), value.hi);
    }
  }

  return localized;
}

// ─── Helper Functions ──────────────────────────────────────────────────────────

/**
 * Retrieve a translation string by key from the dictionary.
 * Falls back to English, then to the key itself.
 */
export function t(language: Language, key: string, fallback?: string, params?: Record<string, string | number>): string {
  let val = translations[language]?.[key] || translations.en?.[key] || fallback || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      let displayVal = String(v);
      if (k === 'diseaseName') {
        displayVal = localizeDiseaseName(displayVal, language);
      }
      val = val.replace(new RegExp(`{${k}}`, 'g'), displayVal);
    });
  }
  return val;
}

/**
 * Format a Date-like input as a localized date string.
 * English: "03 July 2026"
 * Hindi:   "03 जुलाई 2026"
 */
export function formatLocalDate(dateInput: any, language: Language): string {
  if (!dateInput) return '';
  const d = new Date(dateInput);
  if (isNaN(d.getTime())) return '';

  const hindiMonths = [
    'जनवरी', 'फरवरी', 'मार्च', 'अप्रैल', 'मई', 'जून',
    'जुलाई', 'अगस्त', 'सितंबर', 'अक्टूबर', 'नवंबर', 'दिसंबर'
  ];
  const englishMonths = [
    'January', 'February', 'March', 'April', 'May', 'June',
    'July', 'August', 'September', 'October', 'November', 'December'
  ];

  const day = String(d.getDate()).padStart(2, '0');
  const months = language === 'hi' ? hindiMonths : englishMonths;
  return `${day} ${months[d.getMonth()]} ${d.getFullYear()}`;
}

/**
 * Get the locale string for toLocaleDateString and number formatting.
 */
export function getLocale(language: Language): string {
  return language === 'hi' ? 'hi-IN' : 'en-US';
}

/**
 * Save language preference to localStorage (user-specific or global).
 */
export function saveLanguagePreference(language: Language, userId?: string): void {
  if (userId) {
    localStorage.setItem(`twin_lang_${userId}`, language);
  }
  localStorage.setItem('twin_lang', language);
}

/**
 * Load language preference from localStorage (user-specific or global).
 */
export function loadLanguagePreference(userId?: string): Language {
  if (userId) {
    const userPref = localStorage.getItem(`twin_lang_${userId}`);
    if (userPref === 'en' || userPref === 'hi') return userPref;
  }
  const globalPref = localStorage.getItem('twin_lang');
  if (globalPref === 'en' || globalPref === 'hi') return globalPref;
  return 'en';
}
