/**
 * Returns true if the search term matches a crop name in any language.
 */
export function bilingualCropMatch(cropName: string, searchTerm: string): boolean {
  if (!cropName || !searchTerm) return false;
  const lowerCrop = cropName.toLowerCase().trim();
  const lowerSearch = searchTerm.toLowerCase().trim();

  // Direct substring match
  if (lowerCrop.includes(lowerSearch) || lowerSearch.includes(lowerCrop)) return true;

  // Cross-language match
  for (const aliases of Object.values(cropSearchMap)) {
    const cropInAliases = aliases.some(a => lowerCrop.includes(a.toLowerCase()));
    const searchInAliases = aliases.some(a => a.toLowerCase().includes(lowerSearch) || lowerSearch.includes(a.toLowerCase()));
    if (cropInAliases && searchInAliases) return true;
  }
  return false;
}

